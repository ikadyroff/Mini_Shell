#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*c;

	if (lst == NULL || f == NULL)
		return ;
	c = lst;
	while (c != NULL)
	{
		(*f)(c->content);
		c = c->next;
	}
}
