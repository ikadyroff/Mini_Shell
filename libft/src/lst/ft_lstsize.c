#include "libft.h"

int	ft_lstsize(t_list *list)
{
	int		i;
	t_list	*c;

	if (list == NULL)
		return (0);
	i = 1;
	c = list;
	while (c->next != 0)
	{
		c = c->next;
		i++;
	}
	return (i);
}
