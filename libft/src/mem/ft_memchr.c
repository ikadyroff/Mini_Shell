#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*buf;

	buf = s;
	while (n > 0)
	{
		if (*buf == (unsigned char)c)
			return ((void *)buf);
		buf++;
		n--;
	}
	return (NULL);
}
